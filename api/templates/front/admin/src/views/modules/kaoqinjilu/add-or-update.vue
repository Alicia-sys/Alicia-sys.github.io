<script>
	import imgAdd from "@/components/common/img";
	export default {
		data() {
			return {
				id: '',
				type: '',
			
				userface : '',
				faceMatchFlag: false,
			
				ro:{
					shiyanshibianhao : false,
					shiyanshimingcheng : false,
					shiyanshifenlei : false,
					shiyanshiguimo : false,
					touxiang : false,
					qiandaoshijian : false,
					qiandaobeizhu : false,
					yonghuzhanghao : false,
					yonghuxingming : false,
					yonghuleixing : false,
				},
			
				ruleForm: {
					shiyanshibianhao: '',
					shiyanshimingcheng: '',
					shiyanshifenlei: '',
					shiyanshiguimo: '',
					touxiang: '',
					qiandaoshijian: '',
					qiandaobeizhu: '',
					yonghuzhanghao: '',
					yonghuxingming: '',
					yonghuleixing: '',
				},

				rules: {
					shiyanshibianhao: [
					],
					shiyanshimingcheng: [
					],
					shiyanshifenlei: [
					],
					shiyanshiguimo: [
					],
					touxiang: [
					],
					qiandaoshijian: [
					],
					qiandaobeizhu: [
					],
					yonghuzhanghao: [
					],
					yonghuxingming: [
					],
					yonghuleixing: [
					],
				},
			};
		},
		props: ["parent"],
		computed: {



		},
		components: {
			imgAdd
		},
		created() {
			this.ruleForm.qiandaoshijian = this.getCurDateTime()
		},
		methods: {
			imgAddClick(){
				this.$refs.imgAdd.onTake()
			},
			imgChange(e){
				this.ruleForm.touxiang = 'upload/' + e
			},
			// 下载
			download(file){
				window.open(`${file}`)
			},
			// 初始化
			init(id,type) {
				if (id) {
					this.id = id;
					this.type = type;
				}
				if(this.type=='info'||this.type=='else'||this.type=='msg'){
					this.info(id);
				}else if(this.type=='logistics'){
					for(let x in this.ro) {
						this.ro[x] = true
					}
					this.logistics=false;
					this.info(id);
				}else if(this.type=='cross'){
					var obj = this.$storage.getObj('crossObj');
					for (var o in obj){
						if(o=='shiyanshibianhao'){
							this.ruleForm.shiyanshibianhao = obj[o];
							this.ro.shiyanshibianhao = true;
							continue;
						}
						if(o=='shiyanshimingcheng'){
							this.ruleForm.shiyanshimingcheng = obj[o];
							this.ro.shiyanshimingcheng = true;
							continue;
						}
						if(o=='shiyanshifenlei'){
							this.ruleForm.shiyanshifenlei = obj[o];
							this.ro.shiyanshifenlei = true;
							continue;
						}
						if(o=='shiyanshiguimo'){
							this.ruleForm.shiyanshiguimo = obj[o];
							this.ro.shiyanshiguimo = true;
							continue;
						}
						if(o=='touxiang'){
							this.ruleForm.touxiang = obj[o];
							this.ro.touxiang = true;
							continue;
						}
						if(o=='qiandaoshijian'){
							this.ruleForm.qiandaoshijian = obj[o];
							this.ro.qiandaoshijian = true;
							continue;
						}
						if(o=='qiandaobeizhu'){
							this.ruleForm.qiandaobeizhu = obj[o];
							this.ro.qiandaobeizhu = true;
							continue;
						}
						if(o=='yonghuzhanghao'){
							this.ruleForm.yonghuzhanghao = obj[o];
							this.ro.yonghuzhanghao = true;
							continue;
						}
						if(o=='yonghuxingming'){
							this.ruleForm.yonghuxingming = obj[o];
							this.ro.yonghuxingming = true;
							continue;
						}
						if(o=='yonghuleixing'){
							this.ruleForm.yonghuleixing = obj[o];
							this.ro.yonghuleixing = true;
							continue;
						}
					}
				}
				// 获取用户信息
				this.$http({
					url: `${this.$storage.get('sessionTable')}/session`,
					method: "get"
				}).then(({ data }) => {
					if (data && data.code === 0) {
						if(this.$storage.get("role")=="管理员") {
							this.faceMatchFlag= true;
						}
						this.userface = data.data.touxiang;
						var json = data.data;
						if(((json.yonghuzhanghao!=''&&json.yonghuzhanghao) || json.yonghuzhanghao==0) && this.$storage.get("role")!="管理员"){
							this.ruleForm.yonghuzhanghao = json.yonghuzhanghao
							this.ro.yonghuzhanghao = true;
						}
						if(((json.yonghuxingming!=''&&json.yonghuxingming) || json.yonghuxingming==0) && this.$storage.get("role")!="管理员"){
							this.ruleForm.yonghuxingming = json.yonghuxingming
							this.ro.yonghuxingming = true;
						}
						if(((json.yonghuleixing!=''&&json.yonghuleixing) || json.yonghuleixing==0) && this.$storage.get("role")!="管理员"){
							this.ruleForm.yonghuleixing = json.yonghuleixing
							this.ro.yonghuleixing = true;
						}
					} else {
						this.$message.error(data.msg);
					}
				});
			
			},
			// 多级联动参数

			info(id) {
				this.$http({
					url: `kaoqinjilu/info/${id}`,
					method: "get"
				}).then(({ data }) => {
					if (data && data.code === 0) {
						this.ruleForm = data.data;
						//解决前台上传图片后台不显示的问题
						let reg=new RegExp('../../../upload','g')//g代表全部
					} else {
						this.$message.error(data.msg);
					}
				});
			},
			faceMatchHandle() {
				if(this.ruleForm.touxiang!=null) {
					this.ruleForm.touxiang = this.ruleForm.touxiang.replace(new RegExp(this.$base.url,"g"),"");
				}
				let params = {
					face1:this.userface.replace("upload/",""),
					face2:this.ruleForm.touxiang.replace("upload/",""),
				} 
				this.$http({
					url: `matchFace`,
					method: "get",
					params: params
				}).then(({ data }) => {
					if (data && data.code === 0) {
						if(data.score>60) {
							this.faceMatchFlag = true;
							this.$message({
								message: "匹配成功",
								type: "success",
								duration: 1500,
							});
						} else {
							this.faceMatchFlag = false;
							this.$message.error("匹配失败");
						}
					} else {
						this.faceMatchFlag = false;
						this.$message.error("匹配失败");
					}
				});
			},

			// 提交
			async onSubmit() {
					if(this.ruleForm.touxiang!=null) {
						this.ruleForm.touxiang = this.ruleForm.touxiang.replace(new RegExp(this.$base.url,"g"),"");
					}
					var objcross = this.$storage.getObj('crossObj');
					await this.$refs["ruleForm"].validate(async valid => {
						if (valid) {
							if(this.type=='cross'){
								var statusColumnName = this.$storage.get('statusColumnName');
								var statusColumnValue = this.$storage.get('statusColumnValue');
								if(statusColumnName!='') {
									var obj = this.$storage.getObj('crossObj');
									if(statusColumnName && !statusColumnName.startsWith("[")) {
										for (var o in obj){
											if(o==statusColumnName){
												obj[o] = statusColumnValue;
											}
										}
										var table = this.$storage.get('crossTable');
										await this.$http({
											url: `${table}/update`,
											method: "post",
											data: obj
										}).then(({ data }) => {});
									}
								}
							}
							
							await this.$http({
								url: `kaoqinjilu/${!this.ruleForm.id ? "save" : "update"}`,
								method: "post",
								data: this.ruleForm
							}).then(async ({ data }) => {
								if (data && data.code === 0) {
									this.$message({
										message: "操作成功",
										type: "success",
										duration: 1500,
										onClose: () => {
											this.parent.showFlag = true;
											this.parent.addOrUpdateFlag = false;
											this.parent.kaoqinjiluCrossAddOrUpdateFlag = false;
											this.parent.search();
											this.parent.contentStyleChange();
										}
									});
								} else {
									this.$message.error(data.msg);
								}
							});
						}
					});
			},
			// 获取uuid
			getUUID () {
				return new Date().getTime();
			},
			// 返回
			back() {
				this.parent.showFlag = true;
				this.parent.addOrUpdateFlag = false;
				this.parent.kaoqinjiluCrossAddOrUpdateFlag = false;
				this.parent.contentStyleChange();
			},
			touxiangUploadChange(fileUrls) {
				this.ruleForm.touxiang = fileUrls;
			},
		}
	};
</script>
